MERGE INTO FT L
USING (SELECT DISTINCT(D.ACCOUNT_NUMBER),D.MOBILE_NUMBER, D.IFSC_CODE, D.URN_NUMBER, D.BENEFICIARY_ACCOUNT_NUMBER,
D.SANCTION_AMOUNT, D.INSURANCE_PERC, D.TOTAL_CHARGES1, D.CUSTOMER_FUNDS_TRANSFER_AMT, D.PENNY_DROP, D.VENDOR_FUND_TRANSFER_AMOUNT, D.PENNY_DROP1,
D.LO_LOAN_AMT_GOAL_AMT, D.LO_CHARGES_AGR_IN_RS_GL_IN_PS, D.FUND_TRANSFER_AMOUNT, D.CORRECT_AL, D.WRONG_AL
FROM FT_REJECTED_FT D) TMP
ON (L.ACCOUNT_NUMBER = TMP.ACCOUNT_NUMBER AND L.FT_STATUS='REJECTED_FT' AND L.CATEGORY='ATYATI')
WHEN MATCHED THEN
UPDATE SET L.MOBILE_NUMBER=TMP.MOBILE_NUMBER,
L.IFSC_CODE=TMP.IFSC_CODE,
L.URN_NUMBER=TMP.URN_NUMBER,
L.BENEFICIARY_ACCOUNT_NUMBER=TMP.BENEFICIARY_ACCOUNT_NUMBER,
L.SANCTION_AMOUNT=TMP.SANCTION_AMOUNT,
L.INSURANCE_PERC=TMP.INSURANCE_PERC,
L.TOTAL_CHARGES1=TMP.TOTAL_CHARGES1,
L.CUSTOMER_FUNDS_TRANSFER_AMT=TMP.CUSTOMER_FUNDS_TRANSFER_AMT,
L.PENNY_DROP=TMP.PENNY_DROP,
L.VENDOR_FUND_TRANSFER_AMOUNT=TMP.VENDOR_FUND_TRANSFER_AMOUNT,
L.PENNY_DROP1=TMP.PENNY_DROP1,
L.LO_LOAN_AMT_GOAL_AMT=TMP.LO_LOAN_AMT_GOAL_AMT,
L.LO_CHARGES_AGR_IN_RS_GL_IN_PS=TMP.LO_CHARGES_AGR_IN_RS_GL_IN_PS,
L.FUND_TRANSFER_AMOUNT=TMP.FUND_TRANSFER_AMOUNT;
commit;


