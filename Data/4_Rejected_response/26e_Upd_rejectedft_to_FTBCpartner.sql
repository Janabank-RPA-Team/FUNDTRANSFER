MERGE INTO FT L
USING (SELECT DISTINCT(D.ACCOUNT_NUMBER), D.LO_LOAN_AMT_GOAL_AMT, D.LO_CHARGES_AGR_IN_RS_GL_IN_PS,D.FUND_TRANSFER_AMOUNT, D.MOBILE_NUMBER,D.IFSC_CODE,
D.URN_NUMBER, D.BENEFICIARY_ACCOUNT_NUMBER, D.ACCOUNT_HOLDER, D.AMOUNT
FROM FT_REJECTED_FT D) TMP
ON (L.ACCOUNT_NUMBER = TMP.ACCOUNT_NUMBER AND L.FT_STATUS='REJECTED_FT' AND L.CATEGORY='BC_PARTNER')
WHEN MATCHED THEN
UPDATE SET L.MOBILE_NUMBER=TMP.MOBILE_NUMBER,
L.IFSC_CODE=TMP.IFSC_CODE,
L.URN_NUMBER=TMP.URN_NUMBER,
L.BENEFICIARY_ACCOUNT_NUMBER=TMP.BENEFICIARY_ACCOUNT_NUMBER,
L.LO_LOAN_AMT_GOAL_AMT=TMP.LO_LOAN_AMT_GOAL_AMT,
L.LO_CHARGES_AGR_IN_RS_GL_IN_PS=TMP.LO_CHARGES_AGR_IN_RS_GL_IN_PS,
L.FUND_TRANSFER_AMOUNT=TMP.FUND_TRANSFER_AMOUNT;
commit;