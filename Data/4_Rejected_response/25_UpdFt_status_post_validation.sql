UPDATE FT_REJECTED_FT 
SET FT_STATUS=CASE
WHEN STD_LENGTH_CHECK='NO' AND SPECIAL_CHAR_CHECK='NO' AND IFSC_CODE_CHECK='YES' AND CASA_CHECK='NO' AND 
MOBILE_NUMBER_CHECK='YES' AND LAN_CHECK='YES' THEN 'APPROVED_FT' 
WHEN STD_LENGTH_CHECK='YES' OR SPECIAL_CHAR_CHECK='YES' OR IFSC_CODE_CHECK='NO' OR CASA_CHECK='YES' OR MOBILE_NUMBER_CHECK='NO'
 OR LAN_CHECK='NO' THEN 'REJECTED_FT'
END
WHERE ACCOUNT_NUMBER IS NOT NULL;
commit;