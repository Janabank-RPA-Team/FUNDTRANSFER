UPDATE FT
SET CATEGORY=CASE
WHEN ACCOUNT_NUMBER IN (SELECT DISTINCT(LOAN_ACCOUNT_NUMBER) FROM FT_ATYATI) THEN 'ATYATI'
WHEN ACCOUNT_NUMBER IN (SELECT DISTINCT(LOAN_ACCOUNT_NO) FROM FT_BC_PARTNER) THEN 'BC_PARTNER'
WHEN ACCOUNT_NUMBER IN (SELECT DISTINCT(CBS_ACCOUNT_NUMBER) FROM FT_INTERNAL_FT_REQ) THEN 'INTERNAL_FT'
WHEN ACCOUNT_NUMBER IN (SELECT DISTINCT(CBS_LOAN_ACCOUNT_NUMBER) FROM FT_INTERNAL_DD_REQ) THEN 'INTERNAL_DD'
END
WHERE ACCOUNT_NUMBER IS NOT NULL;  
COMMIT;
