UPDATE FT
SET REASON= CASE
WHEN IFSC_CODE_CHECK='NO' AND CASA_CHECK='YES' THEN 'Invalid IFSC Code and CASA number'
WHEN IFSC_CODE_CHECK='NO' AND CASA_CHECK='YES' AND MOBILE_NUMBER_CHECK='NO' THEN 'Invalid IFSC Code ,CASA number, Mobile number'
WHEN IFSC_CODE_CHECK='NO' AND CASA_CHECK='YES' AND MOBILE_NUMBER_CHECK='NO' AND LAN_CHECK='NO' THEN 'Invalid IFSC Code, ,CASA number, Mobile number, LAN'
WHEN IFSC_CODE_CHECK='NO' AND CASA_CHECK='YES' AND MOBILE_NUMBER_CHECK='NO' AND LAN_CHECK='NO' AND SPECIAL_CHAR_CHECK='YES' THEN 'Invalid IFSC Code, ,CASA number, Mobile number, LAN, Special char'
WHEN IFSC_CODE_CHECK='NO' AND CASA_CHECK='YES' AND MOBILE_NUMBER_CHECK='NO' AND LAN_CHECK='NO' AND SPECIAL_CHAR_CHECK='YES' AND LENGTH_CHECK='YES'
THEN 'Invalid IFSC Code, CASA number, Mobile number, LAN. Check Special char, Length'
WHEN IFSC_CODE_CHECK='NO' AND CASA_CHECK='YES' AND MOBILE_NUMBER_CHECK='NO' AND LAN_CHECK='NO' AND SPECIAL_CHAR_CHECK='YES' AND LENGTH_CHECK='YES' AND FT_AMOUNT_MATCHING!='0'
THEN 'Invalid IFSC Code, CASA number, Mobile number, LAN. Check Special char, Length. Check Charges'
END;
COMMIT;