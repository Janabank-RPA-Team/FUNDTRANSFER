UPDATE FT
SET FT_STATUS=CASE
WHEN LENGTH_CHECK='NO' AND SPECIAL_CHAR_CHECK='NO' AND IFSC_CODE_CHECK='YES' AND CASA_CHECK='NO' AND 
MOBILE_NUMBER_CHECK='YES' AND LAN_CHECK='YES' AND FT_AMOUNT_MATCHING='0' THEN 'APPROVED_FT' 
WHEN LENGTH_CHECK='YES' OR SPECIAL_CHAR_CHECK='YES' OR IFSC_CODE_CHECK='NO' OR CASA_CHECK='YES' OR 
MOBILE_NUMBER_CHECK='NO' OR LAN_CHECK='NO' OR FT_AMOUNT_MATCHING!='0' THEN 'REJECTED_FT'
END
WHERE ACCOUNT_NUMBER IS NOT NULL;
COMMIT;